
public class IfElseIfElseCondition {
	public static void main(String[] args) {
		int nombre = 10;
	
		
		if (nombre > 15) {
			
			System.out.println("Le nombre est >= 15 ");
			
			}else if (nombre > 10) {
				
				System.out.println("Le nombre est > 10");
				
				}else if (nombre == 10) {
					
					System.out.println("Le nombre est = 10");				
					
					}else {
						
						System.out.println("Le nombre est < 10");		
			
		}
		
	}
	
}
