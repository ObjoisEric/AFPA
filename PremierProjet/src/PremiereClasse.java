public class PremiereClasse {

	public static void main(String[] args) {
		// String -> chaine de caractères "_________________"       
		// Affiche une chaine de caratères -> String
		System.out.println("Bonjour");
		System.out.println("Bonsoir");		
		
		// int -> Entier 5
		// Afficher un nombre entier -> Int
		System.out.println(12);
		
		// float -> nombre décimal 12,5
		// Afficher un nombre avec une , -> Float
		System.out.println(12.2);
		
		// char -> caractère '_'
		// Afficher un char -> Char
		System.out.println('a');
		
		// boolean -> true or false
		// Affiche un boolean
		System.out.println(true);
		System.out.println(false);
		
		// Résultat d'un cacul		
		System.out.println(12 * 12);
		System.out.println(12 - 12);
		System.out.println(12 + 12);
		System.out.println(12 / 12);
		
		// Afficher plusieurs données
		System.out.println("Bonjour "+"monsieur");
		System.out.println("Bonjour "+"monsieur "+ 124);
		
		System.out.println("12 * 12 = " + (12 * 12));
		System.out.println("12 - 12 = " + (12 - 12));
		System.out.println("12 + 12 = " + (12 + 12));
		System.out.println();
		System.out.println("12 / 12 = " + (12 / 12));
		System.out.println();
		
		// System.out.print();
		System.out.println("Utilisation de System.out.println");
		System.out.print("bonjour");
		System.out.print("bonsoir");
	
		// System.out.println("Bonjour");============= System.out.print(); System.out.print();
		System.out.println();
		System.out.print("12 * 12 = " + (12 * 12));
		
		
		
		
		
		
		
		
		
		
		
		
	}

}